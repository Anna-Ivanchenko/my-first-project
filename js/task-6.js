function calcSimpleBMI(weight, height) {
    const w = parseFloat(weight.replace(',', '.')); //–ø–µ—Ä–µ—Ç–≤–æ—Ä —Ä—è–¥–æ–∫ –≤ —Ü–∏—Ñ—Ä–∏ —ñ –∫–æ–º–∏ –≤ –∫—Ä–∞–ø–∫—É
    const h = parseFloat(height.replace(',', '.')); //–ø–µ—Ä–µ—Ç–≤–æ—Ä —Ä—è–¥–æ–∫ –≤ —Ü–∏—Ñ—Ä–∏ —ñ –∫–æ–º–∏ –≤ –∫—Ä–∞–ø–∫–∏

    // 1Ô∏è‚É£ –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞ –Ω–∞ –ø–æ–º–∏–ª–∫–æ–≤—ñ –∑–Ω–∞—á–µ–Ω–Ω—è
    // –Ø–∫—â–æ —Ä–∞–ø—Ç–æ–º –ø–µ—Ä–µ–¥–∞–¥—É—Ç—å 'abc' –∞–±–æ –ø–æ—Ä–æ–∂–Ω—ñ–π —Ä—è–¥–æ–∫:
    if (isNaN(w) || isNaN(h)) {
        console.log(`–û–¥–Ω–µ –∑ –∑–Ω–∞—á–µ–Ω—å –Ω–µ–∫–æ—Ä–µ–∫—Ç–Ω–µ!`);
        return null;
    }

    // 2Ô∏è‚É£ –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞, —â–æ–± —á–∏—Å–ª–∞ –±—É–ª–∏ –±—ñ–ª—å—à–µ 0
    if (w <= 0 || h <= 0) {
        console.log(`–ó—Ä—ñ—Å—Ç –∞–±–æ –≤–∞–≥–∞ –º–∞—é—Ç—å –±—É—Ç–∏ –±—ñ–ª—å—â—ñ –∑–∞ 0!`);
        return null;
    }

    const bmi = (w / h ** 2); //–ø—ñ–¥—Å—Ç–∞–≤–ª—è—î–º–æ —Ñ–æ—Ä–º—É–ª—É BMI
    return bmi; //–ø–æ–≤–µ—Ä—Ç–∞—î–º–æ —Ä–µ–∑—É–ª—å—Ç–∞—Ç
}
