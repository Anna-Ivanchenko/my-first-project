function convertCelsiusToFahrenheit(temp) {
    const tempValue = parseFloat(temp.replace(',', '.')); //–ø–µ—Ä–µ—Ç–≤–æ—Ä –∑ —Ä—è–¥–∫–∞ –Ω–∞ —á–∏—Å–ª–æ —ñ –∫–æ–º—É –Ω–∞ –∫—Ä–∞–ø–∫—É

    // 1Ô∏è‚É£ –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞ –Ω–∞ –ø–æ–º–∏–ª–∫–æ–≤—ñ –∑–Ω–∞—á–µ–Ω–Ω—è
    // –Ø–∫—â–æ —Ä–∞–ø—Ç–æ–º –ø–µ—Ä–µ–¥–∞–¥—É—Ç—å 'abc' –∞–±–æ –ø–æ—Ä–æ–∂–Ω—ñ–π —Ä—è–¥–æ–∫:
    if (isNaN(tempValue)) {
        console.log(`–ù–µ–∫–æ—Ä–µ–∫—Ç–Ω—ñ –¥–∞–Ω—ñ!`);
        return null;
    }
   
    const convertTempValue = ((tempValue * 9 / 5) + 32).toFixed(1); //–ø—ñ–¥—Å—Ç–∞–≤–ª —Ñ–æ—Ä–º—É–ª—É –¥–ª—è —Ä–æ–∑—Ä–∞—Ö—É–Ω–∫—É —ñ –æ–∫—Ä—É–≥–ª –¥–æ 1-–≥–æ –∑–Ω–∞–∫–∞
    return Number(convertTempValue); //–ø–æ–≤–µ—Ä—Ç–∞—î–º–æ —Ä–µ–∑—É–ª—å—Ç–∞—Ç
}
