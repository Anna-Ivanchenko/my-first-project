// –°—Ç–≤–æ—Ä–∏ —Ñ—É–Ω–∫—Ü—ñ—é calcAverage(a, b, c), —è–∫–∞ –ø—Ä–∏–π–º–∞—î —Ç—Ä–∏ –∑–Ω–∞—á–µ–Ω–Ω—è —É –≤–∏–≥–ª—è–¥—ñ —Ä—è–¥–∫—ñ–≤.
// –ö–æ–∂–Ω–µ –∑–Ω–∞—á–µ–Ω–Ω—è –º–æ–∂–µ –º—ñ—Å—Ç–∏—Ç–∏ –¥–µ—Å—è—Ç–∫–æ–≤—ñ —á–∏—Å–ª–∞.
// –§—É–Ω–∫—Ü—ñ—è –ø–æ–≤–∏–Ω–Ω–∞:
// –ø–µ—Ä–µ—Ç–≤–æ—Ä–∏—Ç–∏ –≤—Å—ñ –∑–Ω–∞—á–µ–Ω–Ω—è –≤ —á–∏—Å–ª–∞;
// –∑–Ω–∞–π—Ç–∏ —Å–µ—Ä–µ–¥–Ω—î –∞—Ä–∏—Ñ–º–µ—Ç–∏—á–Ω–µ;
// –ø–æ–≤–µ—Ä–Ω—É—Ç–∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç, –æ–∫—Ä—É–≥–ª–µ–Ω–∏–π –¥–æ –æ–¥–Ω–æ–≥–æ –∑–Ω–∞–∫–∞ –ø—ñ—Å–ª—è –∫–æ–º–∏.

function calcAverage(a, b, c) {

    //–º—ñ–Ω—è—î–º–æ –∫–æ–º—É –Ω–∞ –∫—Ä–∞–ø–∫—É —ñ –ø–µ—Ä–µ—Ç–≤–æ—Ä—é—î–º–æ –Ω–∞ —á–∏—Å–ª–∞
    const an = parseFloat(a.replace(',', '.'));
    const bn = parseFloat(b.replace(',', '.'));
    const cn = parseFloat(c.replace(',', '.'));

    //–∑–Ω–∞—Ö–æ–¥–∏–º–æ —Å–µ—Ä–µ–¥–Ω—î –∞—Ä–∏—Ñ–º–µ—Ç–∏—á–Ω–µ
    //–æ–∫—Ä—É–≥–ª. –¥–æ –æ–¥–Ω–æ–≥–æ –∑–Ω–∞–∫–∞ –ø—ñ—Å–ª—è –∫–æ–º–∏
    //–ø–µ—Ä–µ—Ç–≤–æ—Ä—é—î–º–æ –Ω–∞ —á–∏—Å–ª–∞, –±–æ .toFixed() –∑–Ω–æ–≤—É –ø–µ—Ä–µ—Ç–≤–æ—Ä—é—î –Ω–∞ —Ä—è–¥–æ–∫
    return Number(((an + bn + cn) / 3).toFixed(1));
}
//–ø–µ—Ä–µ–¥–∞—î–º–æ –ø–∞—Ä–∞–º–µ—Ç—Ä–∏
const totalAverage = calcAverage('24.5', '18,44', '8.587');
console.log(totalAverage);

