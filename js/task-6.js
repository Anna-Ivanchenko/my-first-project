// –ø–æ–≤–µ—Ä–Ω—É—Ç–∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç, –æ–∫—Ä—É–≥–ª–µ–Ω–∏–π –¥–æ –¥–≤–æ—Ö –∑–Ω–∞–∫—ñ–≤ –ø—ñ—Å–ª—è –∫–æ–º–∏.

function calcDiscountPrice(price, discount) {
    const priceNumb = parseFloat(price);
    const discountNumb = parseFloat(discount);
    const totalPrice = (priceNumb * (1 - discountNumb / 100)).toFixed(2)

    return Number(totalPrice);
}
