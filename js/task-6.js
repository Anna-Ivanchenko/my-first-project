// –§—É–Ω–∫—Ü—ñ—è –ø–æ–≤–∏–Ω–Ω–∞:
// –∑–∞–º—ñ–Ω–∏—Ç–∏ –∫–æ–º—É –Ω–∞ –∫—Ä–∞–ø–∫—É (—è–∫—â–æ –≤–æ–Ω–∞ —î);
// –ø–µ—Ä–µ—Ç–≤–æ—Ä–∏—Ç–∏ –∑–Ω–∞—á–µ–Ω–Ω—è –≤ —á–∏—Å–ª–∞;
// –æ–±—á–∏—Å–ª–∏—Ç–∏ –ø–ª–æ—â—É –ø—Ä—è–º–æ–∫—É—Ç–Ω–∏–∫–∞ –∑–∞ —Ñ–æ—Ä–º—É–ª–æ—é —à–∏—Ä–∏–Ω–∞ √ó –≤–∏—Å–æ—Ç–∞;
// –ø–æ–≤–µ—Ä–Ω—É—Ç–∏ —Ä–µ–∑—É–ª—å—Ç–∞—Ç, –æ–∫—Ä—É–≥–ª–µ–Ω–∏–π –¥–æ –¥–≤–æ—Ö –∑–Ω–∞–∫—ñ–≤ –ø—ñ—Å–ª—è –∫–æ–º–∏.

function calcRectangleArea(width, height) {    
    //–∑–∞–º—ñ–Ω—è—î–º–æ –∫–æ–º—É –Ω–∞ –∫—Ä–∞–ø–∫—É
    const w = width.replace(',', '.');
    const h = height.replace(',', '.');

    // –ø–µ—Ä–µ—Ç–≤–æ—Ä—é—î–º–æ —Ä—è–¥–∫–∏ –≤ —á–∏—Å–ª–∞
    const wNumber = parseFloat(w); //—á–∏—Å–ª–æ —à–∏—Ä–∏–Ω–∏
    const hNumber = parseFloat(h); //—á–∏—Å–ª–æ –≤–∏—Å–æ—Ç–∏

    //–æ–±—á–∏—Å–ª–∏—Ç–∏ –ø–ª–æ—â—É –ø—Ä—è–º–æ–∫—É—Ç–Ω–∏–∫–∞ –∑–∞ —Ñ–æ—Ä–º—É–ª–æ—é —à–∏—Ä–∏–Ω–∞ √ó –≤–∏—Å–æ—Ç–∞; –æ–∫—Ä—É–≥–ª—é—î–º–æ
    return (wNumber * hNumber).toFixed(2);
}
